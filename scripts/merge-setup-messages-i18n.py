def parse_bundle(path: Path) -> dict[str, str]:
    out: dict[str, str] = {}
    for line in path.read_text(encoding="utf-8").splitlines():
        line = line.strip()
        if not line or line.startswith("#") or "=" not in line:
            continue
        key, _, value = line.partition("=")
        out[key.strip()] = value.strip()
    return out


def strip_setup_keys(text: str) -> str:
    return re.sub(r"^(setup|upgrade)\.[^\n]+\n", "", text, flags=re.MULTILINE)


def merge_file(name: str, zh: dict[str, str]) -> None:
    path = I18N / name
    body = strip_setup_keys(path.read_text(encoding="utf-8")).rstrip()
    overrides = LOCALES.get(name)
    lines = [body, ""]
    for key in sorted(zh.keys()):
        if overrides is None:
            value = zh[key]
        else:
            value = overrides.get(key, zh[key])
        lines.append(f"{key}={value}")
    lines.append("")
    path.write_text("\n".join(lines), encoding="utf-8")
    print(f"merged {name}")


def main() -> None:
    zh = parse_bundle(BUNDLE)
    for name in LOCALES:
        merge_file(name, zh)


if __name__ == "__main__":
    main()
